export function sumarNumeros(numero1, numero2) {
    return numero1 + numero2;
}

export function restarNumeros(numero1, numero2) {
    return numero1 - numero2;
}

export function multiplicarNumeros(numero1, numero2) {
    return numero1 * numero2;
}

export function dividirNumeros(numero1, numero2) {
    return numero1 / numero2;
}

